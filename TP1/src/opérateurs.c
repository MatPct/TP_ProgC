#include <stdio.h>

int main() {

  int a = 16;
  int b = 3;

  // On affiche les valeurs des deux variables pour plus de clarté sur les résultats ci-dessous.
  printf("Valeur de la variable a : %d\n", a);
  printf("Valeur de la variable b : %d\n", b);
  
  // On affiche le résultat des tests des différents opérateurs arithmétiques.
  printf("L'addition de a et b est égale à : %d\n", a + b);
  printf("La soustraction de a et b est égale à : %d\n", a - b);
  printf("La multiplication de a et b est égale à : %d\n", a * b);
  printf("La division de a par b est égale à : %d\n", a / b);
  printf("Le reste de la division de a par b est égal à : %d\n", a % b);
  printf("Utilisation d'un opérateur de comparaison pour vérifier si a et b sont égaux : %d\n", a == b);
}




